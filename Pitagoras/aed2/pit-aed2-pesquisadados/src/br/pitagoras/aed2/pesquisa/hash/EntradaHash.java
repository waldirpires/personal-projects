package br.pitagoras.aed2.pesquisa.hash;

public class EntradaHash {

	private int chave; // chave da entrada hash
	private int valor; // valor da entrada hash
	
	public EntradaHash(int chave, int valor){
		this.chave = chave;
		this.valor = valor;
	}

	public int getChave() {
		return chave;
	}

	public int getValor() {
		return valor;
	}

	
	
}
